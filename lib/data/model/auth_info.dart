class AuthInfo {
  final String firstName;
  final String lastName;
  final String email;
  final int phoneNumber;
  final int bankAccountNumber;
  final int year;
  final int month;
  final int day;

  AuthInfo({
    required this.firstName,
    required this.lastName,
    required this.email,
    required this.phoneNumber,
    required this.bankAccountNumber,
    required this.year,
    required this.month,
    required this.day,
  });
}
